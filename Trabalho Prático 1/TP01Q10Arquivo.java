public class TP01Q10Arquivo {
    public static void escreverArquivo(String nomeArquivo) throws Exception {
        RandomAccessFile escritor = new RandomAccessFile(new File(nomeArquivo), "rw");

        int quantidadeValoresAGravar = MyIO.readInt();

        // Realiza leitura das entradas e as grava em linhas separadas no arquivo
        for (int indice = 0; indice < quantidadeValoresAGravar; indice++) {
            double valorAGravar = MyIO.readDouble();
            escritor.writeDouble(valorAGravar);
        }

        // fecha o arquivo
        escritor.close();
    }

    public static void lerArquivo(String caminhoArquivo) throws Exception {
        RandomAccessFile leitor = new RandomAccessFile(caminhoArquivo, "r");

        // inicializa posição do leitor na primeira posição do arquivo
        leitor.seek(0);

        // chama método para imprimir recursivamente os valores a partir da posição 0
        imprimirValor(leitor.getFilePointer(), leitor);

        // fecha o arquivo
        leitor.close();
    }

    public static void imprimirValor(long ultimaPosicaoLida, RandomAccessFile leitor) throws Exception {
        double valorLido = leitor.readDouble();

        // enquanto não for atingida a ultima posição do arquivo, continua escrevendo
        // recursivamente o próximo valor
        if (leitor.getFilePointer() < leitor.length()) {
            imprimirValor(leitor.getFilePointer(), leitor);
        }

        // aplica correção para impressão de numeros "inteiros" (sem numeros após a
        // vírgula) para verificação do verde
        if ((int) valorLido == valorLido) {
            MyIO.println((int) valorLido);
        } else {
            MyIO.println(valorLido);
        }
    }

    public static void main(String args[]) throws Exception {
        String caminhoNovoArquivo = "./Arquivo.txt";
        // lê e grava o arquivo
        escreverArquivo(caminhoNovoArquivo);
        // escreve conteudo na tela
        lerArquivo(caminhoNovoArquivo);
    }
}

