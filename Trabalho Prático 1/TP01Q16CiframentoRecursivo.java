public class TP01Q16CiframentoRecursivo {

    public static boolean isFim(String s) {
        return (s.length() >= 3 && s.charAt(0) == 'F' && s.charAt(1) == 'I' && s.charAt(2) == 'M');
    }

    public static String codificarCiframento(int indiceCaracter, String mensagem, int chave) {
        String mensagemCifrada = "";

        if (indiceCaracter < mensagem.length()) {
            int valorCaracterCodificado = mensagem.charAt(indiceCaracter) + chave;
            char caracterCodificado = (char) valorCaracterCodificado;
            mensagemCifrada = caracterCodificado + codificarCiframento(indiceCaracter + 1, mensagem, chave);
        }

        return mensagemCifrada;
    }

    public static void main(String args[]) {
        int posicaoUltimaPalavraLida = 0;
        String[] entradas = new String[1000];
        int chave = 3;

        MyIO.setCharset("ISO-8859-1");

        // Realiza leitura das entradas
        do {
            entradas[posicaoUltimaPalavraLida] = MyIO.readLine();
        } while (!isFim(entradas[posicaoUltimaPalavraLida++]));

        // Desconsidera o índice da entrada com a flag FIM
        posicaoUltimaPalavraLida--;

        // Imprime cada uma das entradas com o ciframento padrão do exercício (3)
        for (int indicePalavra = 0; indicePalavra < posicaoUltimaPalavraLida; indicePalavra++) {
            MyIO.println(codificarCiframento(0, entradas[indicePalavra], chave));
        }
    }
}

